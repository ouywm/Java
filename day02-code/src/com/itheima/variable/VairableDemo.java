package com.itheima.variable;

public class VairableDemo {
    /*
     变量的介绍:内存中的存储空间,空间中记录这经常发生改变的数据,可以变的值
     变量的定义格式:
            数据类型  变量名 = 数据值;
            数据类型: 整数 : int
    */
    public static void main(String[] args) {
        int salary = 12000;
        System.out.println(salary);

        salary = 15000;

        System.out.println(salary);

        int age = 18;

        System.out.println(age);
    }
}
